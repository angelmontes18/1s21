import java.util.Scanner;

public class exa1_1s21{

	public static void main(String[] args) {
		int n = 0;
		Scanner in=new Scanner(System.in);
		n=in.nextInt();
		for (int i = 0; i<n; i++) {//filas
			for (int j = 0;j < n ;j++ ) {//columnas
				System.out.print("* ");
			}
			System.out.println("");
				
			}
			int m=in.nextInt();
			for (int i = 0; i<n; i++) {//filas
			for (int j = 0;j < m ;j++ ) {//columnas
				System.out.print("* ");
			}
			System.out.println("");

			}
		}
	}